int		ft_atoi(char *str)
{
	int i;
	int sign;

	i = 0;
	sign = 1;
	while (*str == '\t' || *str == '\n' || *str == '\v'
			|| *str == '\f' || *str == '\r' || *str == ' ')
		++str;
	while (*str == '+' || *str == '-')
	{
		if (*(str) == '-')
		{
			sign = -sign;
		}
		str++;
	}
	while ('0' <= *str && *str <= '9')
	{
		i = 10 * i;
		i = i + (*str) - '0';
		str++;
	}
	return (sign * i);
}
