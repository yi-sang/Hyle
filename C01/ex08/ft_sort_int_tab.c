void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int j;
	int shortest;

	i = 0;
	while (i++ < size)
	{
		j = 0;
		while (++j < size)
		{
			if (tab[j - 1] > tab[j])
			{
				shortest = tab[j - 1];
				tab[j - 1] = tab[j];
				tab[j] = shortest;
			}
		}
	}
}
